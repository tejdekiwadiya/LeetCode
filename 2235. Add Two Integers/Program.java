// https://leetcode.com/problems/add-two-integers/description/

public class Program {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 5;
        int ans = sum(num1,num2);
        System.out.println(ans);
    }

    public static int sum(int num1, int num2) {
        return num1+num2;
    }
}
